  public function ModuleCallFunction($function, $serviceID, $extraParams = array(), $addonId = 0)
  {
    if ($addonId && !$this->loadByAddonId($addonId)) {
      if (!$this->getAddonModule()) {
        return "success";
      }
      LogActivity::Save("Required Product Module '" . $this->getAddonModule() . "' Missing");
      return "Module Not Found";
    }
    if (!$addonId && !$this->loadByServiceID($serviceID)) {
      if (!$this->getServiceModule()) {
        return "success";
      }
      LogActivity::Save("Required Product Module '" . $this->getServiceModule() . "' Missing");
      return "Module Not Found";
    }
    $params = $this->buildParams();
    if (is_array($extraParams)) {
      $params = array_merge($params, $extraParams);
    }
    $serviceid = (int) $params["serviceid"];
    $userid = (int) $params["userid"];

    $hookabort = false;
    $hookresults = \App\Helpers\Hooks::run_hook("PreModule" . $function, array("params" => $params));
    $hookabort = false;
    foreach ($hookresults as $hookvals) {
      foreach ($hookvals as $k => $v) {
        if ($k == "abortcmd" && $v === true) {
          $hookabort = true;
          $result = "Function Aborted by Action Hook Code";
        }
      }
      if (!$hookabort) {
        $params = array_replace_recursive($params, $hookvals);
      }
