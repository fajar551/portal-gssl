  public function callModule($function, array $params = array())
  {
    if ($this->functionExists($function)) {
      $params = array_merge($this->getParams(), $params);
      $moduleName = $this->getLoadedModule();
      $module = \Module::find($moduleName);
      if ($module) {
        $modName = $module->getName();
        $className = "\\Modules\\Servers\\{$modName}\\Http\\Controllers\\{$modName}Controller";
        $object = new $className();
        return $object->{$function}($params);
      } else {
        return "Module not found";
      }
    }
    return self::FUNCTIONDOESNTEXIST;
  }
  public function call($function, array $params = array())
  {
    $serviceID = (int) $this->serviceID;
    $addonId = (int) $this->addonId;
    $builtParams = array();
    if ($serviceID || $addonId) {
        $builtParams = $this->buildParams();
    }
    switch ($function) {
        case "CreateAccount":
            $action = "create";
            break;
        case "SuspendAccount":
            $action = "suspend";
            break;
        case "UnsuspendAccount":
            $action = "unsuspend";
            break;
        case "TerminateAccount":
            $action = "terminate";
            break;
        case "ChangePassword":
            $action = "changepw";
            break;
        case "ChangePackage":
            $action = "upgrade";
            break;
        default:
            $action = $function;
            break;
    }
    $params["action"] = $action;
    $builtParams = array_merge($builtParams, $params);
    return $this->callModule($function, $builtParams);
  }

  public function functionExists($name)
  {
