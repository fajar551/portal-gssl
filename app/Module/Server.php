  public static function getServerID($servertype, $servergroup)
  {
    $serverid = 0;
    $hostingCountsQuery = "(SELECT COUNT(id) FROM tblhosting WHERE tblhosting.server=tblservers.id AND domainstatus IN ('Active', 'Suspended'))";
    $hostingCountsQuery .= "+ (SELECT COUNT(id) FROM tblhostingaddons WHERE tblhostingaddons.server=tblservers.id AND status IN ('Active', 'Suspended'))";
    if ($servertype && !$servergroup) {
      $result = \App\Models\Server::selectRaw("id,maxaccounts,(" . $hostingCountsQuery . ") AS usagecount")
        ->where(array("type" => $servertype, "active" => "1", "disabled" => "0"));
    //   $data = $result->toArray();
      $data = $result;
      $serverid = $data->value("id") ?? 0;
      $maxaccounts = $data->value("maxaccounts") ?? 0;
      $usagecount = $data->value("usagecount") ?? 0;
      if ($serverid && $maxaccounts <= $usagecount) {
        $result = \App\Models\Server::selectRaw("id,((" . $hostingCountsQuery . ")/maxaccounts) AS percentusage")
        ->where('type', $servertype)
        ->where('id', '!=', $serverid)
        ->where('disabled', "0")
        ->orderBy("percentusage", 'ASC')
        ->first();

        $data = $result->toArray();
        if ($data) {
          $serverid = $data["id"];
          \App\Models\Server::where('type', $servertype)->update(['active', '']);
          \App\Models\Server::where('type', $servertype)->where('id', $serverid)->update(['active', 1]);
        }
      }
    } else {
      if ($servertype) {
        $result = \App\Models\Servergroup::where('id', $servergroup);
        $data = $result;
        $filltype = $data->value("filltype");
        $serverslist = array();
        $result = \App\Models\Servergroupsrel::where('groupid', $servergroup)->get();
        foreach ($result->toArray() as $data) {
          $serverslist[] = $data["serverid"];
        }
        if ($filltype == 1) {
          $result = \App\Models\Server::selectRaw("id,((" . $hostingCountsQuery . ")/maxaccounts) AS percentusage")
            ->whereIn('id', $serverslist)
            ->where('disabled', "0")
            ->orderBy("percentusage", 'ASC')
            ->first();
          $data = $result->toArray();
          $serverid = $data["id"];
        } else {
          if ($filltype == 2) {
            $result = \App\Models\Server::selectRaw("id,maxaccounts,(" . $hostingCountsQuery . ") AS usagecount")
              ->whereIn('id', $serverslist)
              ->where('active', "1")
              ->where('disabled', "0")
              ->first();
            $data = $result->toArray();
            $serverid = $data["id"];
            $maxaccounts = $data["maxaccounts"];
            $usagecount = $data["usagecount"];
            if ($serverid && $maxaccounts <= $usagecount) {
              $result = \App\Models\Server::selectRaw("id,((" . $hostingCountsQuery . ")/maxaccounts) AS percentusage")
                ->whereIn('id', $serverslist)
                ->where('disabled', "0")
                ->where('id', '!=', $serverid)
                ->orderBy("percentusage", 'ASC')
                ->first();
              $data = $result->toArray();
              if ($data) {
                $serverid = $data["id"];
                \App\Models\Server::where('type', $servertype)->update(['active', '']);
                \App\Models\Server::where('type', $servertype)->where('id', $serverid)->update(['active', 1]);
              }
            }
          }
        }
      }
    }
    return $serverid;
  }

  public function doSingleApplicationLinkCall($action)
  {
    if (!$this->isApplicationLinkSupported()) {
        throw new \Exception("Application linking not supported by module");
    }

    if (!$this->isApplicationLinkingEnabled()) {
        throw new \Exception("Application linking not enabled for module");
    }

    if (!in_array($action, array("Create", "Update", "Delete"))) {
        throw new \Exception("Invalid action requested");
    }

    $serviceId = $this->serviceID;
    if (!$serviceId) {
        throw new \Exception("Service ID is required");
    }

    $appLink = \App\Models\AppLink::firstOrCreate([
        "module_type" => $this->getType(),
        "module_name" => $this->getLoadedModule()
    ]);

    $params = $this->buildParams();
    $serverId = $params["serverid"];

    if ($this->functionExists("IsApplicationLinkingSupportedByServer")) {
        $isSupported = $this->call("IsApplicationLinkingSupportedByServer", $this->getServerParams($serverId));
    } else {
        $isSupported = true;
    }

    if (!$isSupported) {
        throw new \Exception("Application linking not supported by server this service is assigned to");
    }

    $appLinksToProvision = array();
    $permittedClientAreaScopes = array();
    if ($this->functionExists("GetSupportedApplicationLinks")) {
        $appLinksToProvision = $this->call("GetSupportedApplicationLinks");
        foreach ($appLink->links()->get() as $link) {
            if ($link->is_enabled) {
                $appLinksToProvision[$link->scope]["label"] = $link->display_label;
                $appLinksToProvision[$link->scope]["order"] = $link->order;
            } else {
                unset($appLinksToProvision[$link->scope]);
            }
        }

        $permittedClientAreaScopeNames = implode(" ", array_keys($appLinksToProvision));
    } else {
        $validScopes = \App\Models\OauthserverScope::where("scope", "LIKE", "clientarea:%")->pluck("scope")->all();
        $permittedClientAreaScopeNames = implode(" ", $validScopes);
    }

    if (strpos($permittedClientAreaScopeNames, "clientarea:sso") === false) {
        $permittedClientAreaScopeNames .= " clientarea:sso";
    }

    $clientCredentialCollection = array();
    if ($action == "Create") {
        // TODO
