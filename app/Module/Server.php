  public function createServerUsername($domain)
  {
    if (!$domain && !Cfg::get("GenerateRandomUsername")) {
      return "";
    }

    if (!Cfg::get("GenerateRandomUsername")) {
      $domain = strtolower($domain);
      $username = preg_replace("/[^a-z]/", "", $domain);
      $username = substr($username, 0, 8);
      $result = Hosting::select(DB::raw('COUNT(*) as total'))->where('username', $username)->first();
      $data = $result->toArray();
      $username_exists = $data['total'];
      $suffix = 0;
      while (0 < $username_exists) {
        $suffix++;
        $trimlength = 8 - strlen($suffix);
        $username = substr($username, 0, $trimlength) . $suffix;
        $result = Hosting::select(DB::raw('COUNT(*) as total'))->where('username', $username)->first();
        $data = $result->toArray();
        $username_exists = $data['total'];
      }
    } else {
      $lowercase = "abcdefghijklmnopqrstuvwxyz";
      $str = "";
      $seeds_count = strlen($lowercase) - 1;
      for ($i = 0; $i < 8; $i++) {
          $str .= $lowercase[rand(0, $seeds_count)];
      }
      $username = "";
      for ($i = 0; $i < 8; $i++) {
          $randomnum = rand(0, strlen($str) - 1);
          $username .= $str[$randomnum];
          $str = substr($str, 0, $randomnum) . substr($str, $randomnum + 1);
      }
      $result = Hosting::select(DB::raw('COUNT(*) as total'))->where('username', $username)->first();
      $data = $result->toArray();
      $username_exists = $data['total'];
      while (0 < $username_exists) {
          $username = "";
          $str = "";
          for ($i = 0; $i < 8; $i++) {
              $str .= $lowercase[rand(0, $seeds_count)];
          }
          for ($i = 0; $i < 8; $i++) {
              $randomnum = rand(0, strlen($str) - 1);
              $username .= $str[$randomnum];
              $str = substr($str, 0, $randomnum) . substr($str, $randomnum + 1);
          }
          $result = Hosting::select(DB::raw('COUNT(*) as total'))->where('username', $username)->first();
          $data = $result->toArray();
          $username_exists = $data['total'];
      }
    }
    return $username;
  }

  public function ServerCreateAccount($serviceID, $addonId = 0)
  {
    $params = $this->ModuleBuildParams($serviceID, $addonId);
    $updateInfo = array();
    if (array_key_exists("service", $params)) {
      if (!$params["username"]) {
        $params["username"] = $params["service"]["username"];
        $updateInfo["username"] = $params["username"];
      }
      if (!$params["domain"]) {
        $params["domain"] = $params["service"]["domain"];
        $updateInfo["domain"] = $params["domain"];
      }
    }

    if (isset($params["username"]) && !$params["username"]) {
      $usernamegenhook = collect(\App\Helpers\Hooks::run_hook("OverrideModuleUsernameGeneration", $params));
      $username = "";
      if (count($usernamegenhook->filter()->all())) {
        foreach ($usernamegenhook as $usernameval) {
          if (is_string($usernameval)) {
            $username = $usernameval;
          }
        }
      }

      if (!$username) {
        $username = $this->createServerUsername($params["domain"]);
      }
      $updateInfo["username"] = $username;
    }

    if (isset($params["password"]) && !$params["password"]) {
      $updateInfo["password"] = (new \App\Helpers\Pwd())->encrypt(self::generateRandomPassword());
    }

    $moduleMetaData = array();
    if ($this->functionExists("MetaData")) {
      $moduleMetaData = $this->call("MetaData");
    }

    if (!array_key_exists("AutoGenerateUsernameAndPassword", $moduleMetaData) || $moduleMetaData["AutoGenerateUsernameAndPassword"] !== false) {
      if ($updateInfo && !$addonId) {
        DB::table('tblhosting')->where("id", $serviceID)->update($updateInfo);
      } else {
        if ($updateInfo) {
          if (array_key_exists("password", $updateInfo)) {
            $updateInfo["password"] = (new \App\Helpers\Pwd())->decrypt($updateInfo["password"]);
          }
          $params["model"]->serviceProperties->save($updateInfo);
        }
      }
    }

    try {
      return $this->ModuleCallFunction("Create", $serviceID, array(), $addonId);
    } catch (\Exception $e) {
      return $e->getMessage();
    }
  }

