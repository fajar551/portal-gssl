        $clientCredential->secret = base64_encode(\phpseclib3\Crypt\Random::string(64));
        $clientCredential->save();
        $clientCredentialCollection[] = $clientCredential;
    } else if ($action == "Delete") {
        $clientCredential = \App\Models\OauthserverClient::where("service_id", $serviceId)->first();
        if (is_null($clientCredential)) {
            throw new \Exception("Existing application link credential not found");
        }

        $clientCredentialCollection[] = $clientCredential;
        $clientCredential->delete();
    }

    $systemUrl = config('app.url_admin');
    $params = $this->getServerParams($serverId) + array(
        "systemUrl" => $systemUrl,
        "tokenEndpoint" => $systemUrl . "oauth/token.php",
        "clientCredentialCollection" => $clientCredentialCollection,
        "appLinks" => $appLinksToProvision
    );

    if (!$this->functionExists($action . "ApplicationLink")) {
        throw new \Exception("Module does not supported requested Application Link Action: \"" . $action . "\"");
    }

    $errors = $this->call($action . "ApplicationLink", $params);
    if (is_array($errors) && 0 < count($errors)) {
        return $errors;
    }

  }

