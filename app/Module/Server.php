  public function buildParams()
  {
    if (is_array($this->params)) {
      $params = $this->params;
      $params = array_merge($params, $this->getServerParams($params["serverid"]));
      $this->params = $params;
      $GLOBALS["moduleparams"] = $params;
      return $params;
    }
    $serviceId = (int) $this->serviceID;
    $addonId = $this->addonId;

    if (!$addonId) {
      try {
        $modelData = $this->modelData;
        if (!$modelData) {
          $modelData = Hosting::with("product", "client")->findOrFail($serviceId);
        }
      } catch (\Exception $e) {
        return [];
      }

      $params = [];
      try {
          $params = $this->buildServiceParams($modelData);
      } catch (\Exception $th) {
          //throw $th;
      }
      $params = array_merge($params, $this->getServerParams($modelData->serverId));
    } else {
      try {
        $modelData = $this->modelData;
        if (!$modelData || !$modelData instanceof \App\Models\Hostingaddon) {
          $modelData = Hostingaddon::with("productAddon", "productAddon.moduleConfiguration", "service", "client")->findOrFail($addonId);
        }
      } catch (\Exception $e) {
        return [];
      }
      $params = $this->buildAddonParams($modelData);
      $params = array_merge($params, $this->getServerParams($modelData->serverId));
      $params["service"] = $this->buildServiceParams($modelData->service);
      $params["service"] = array_merge($params, $this->getServerParams($modelData->service->serverId));
    }
    $client = new \App\Helpers\ClientClass($modelData->client);
    $clientsdetails = $client->getDetails();
    $clientsdetails["state"] = $clientsdetails["statecode"];
    if (!array_key_exists("original", $params)) {
      //TODO: $clientsdetails = foreignChrReplace($clientsdetails);
      $params["clientsdetails"] = $clientsdetails;
    }
    $GLOBALS["moduleparams"] = $params;
    $this->params = $params;
    return $params;
  }

  public function getServerParams($server)
  {
    if (is_numeric($server)) {
      $server = ServerModel::find($server);
    }
    if ($server instanceof \App\Models\Server) {
      $params = array(
            "server" => true,
            "serverid" => (int) $server->id,
            "serverip" => $server->ipaddress,
            "serverhostname" => $server->hostname,
            "serverusername" => \App\Helpers\Sanitize::decode($server->username),
            "serverpassword" => \App\Helpers\Sanitize::decode((new \App\Helpers\Pwd())->decrypt($server->password)),
            "serveraccesshash" => \App\Helpers\Sanitize::decode($server->accesshash),
            "serversecure" => (bool) $server->secure,
            "serverhttpprefix" => "http" . ($server->secure ? "s" : ""),
        );
      $portNum = $server->port;
      if (!$portNum) {
        $portNum = $this->getMetaDataValue("Default" . ($server->secure ? "" : "Non") . "SSLPort");
      }
      $params["serverport"] = $portNum;
    } else {
      $params = array("server" => false, "serverip" => "", "serverhostname" => "", "serverusername" => "", "serverpassword" => "", "serveraccesshash" => "", "serversecure" => false, "serverhttpprefix" => "", "serverport" => "");
    }
    return $params;
  }

