    protected function getSingleSignOnUrl($serverId, $admin)
    {
        $functionToCall = $admin ? "AdminSingleSignOn" : "ServiceSingleSignOn";
        $serverModel = \App\Models\Server::find($serverId);

        if (!$serverModel) {
            throw new \App\Exceptions\Module\SingleSignOnError("Server ID not found.");
        }

        if ($this->load($serverModel->type)) {
            if (!$this->functionExists($functionToCall)) {
                throw new \App\Exceptions\Module\SingleSignOnError("Invalid Server ID for Auto Login.");
            }

            $params = $this->getServerParams($serverModel);
            $results = $this->call($functionToCall, $params);
            if (is_array($results) && isset($results["success"]) && $results["success"] == true) {
                return $results["redirectTo"];
            }

            if (is_array($results) && isset($results["errorMsg"])) {
                throw new \App\Exceptions\Module\SingleSignOnError($results["errorMsg"]);
            }

            throw new \App\Exceptions\Module\SingleSignOnError("Unable to auto-login.");
        }

        throw new \App\Exceptions\Module\SingleSignOnError("Invalid Server Module.");
    }

    public function getServerListForModule()
    {
        $servers = array();
        $disabledServers = array();
        $serverCollection = \App\Models\Server::where("type", "=", $this->addonModule ?: $this->serviceModule)->orderBy("name")->get();

        if ($serverCollection) {
            $hostingCounts = \App\Models\Hosting::selectRaw("domainstatus, count(id) as totalService, server")->whereIn("domainstatus", array("Active", "Suspended"))->groupBy("server")->pluck("server", "totalService");
            $hostingAddonsCounts = \App\Models\Hostingaddon::selectRaw("status, count(id) as totalAddon, server")->whereIn("status", array("Active", "Suspended"))->groupBy("server")->pluck("server", "totalAddon");

            foreach ($serverCollection as $server) {
                $serverId = $server->id;
                $serverName = $server->name;
                $serverMaxAccounts = $server->maxaccounts;
                $disabled = $server->disabled;
                if ($disabled) {
                    $serverName .= " (" . __("admin.emailtplsdisabled") . ")";
                }

                $serverNumberAccounts = ($hostingCounts[$serverId] ?? 0) + ($hostingAddonsCounts[$serverId] ?? 0);
                $label = $serverName . " (" . $serverNumberAccounts . "/" . $serverMaxAccounts . " " . __("admin.fieldsaccounts") . ")";
                if ($disabled) {
                    $disabledServers[$serverId] = $label;
                } else {
                    $servers[$serverId] = $label;
                }
            }
        }

        return array_replace($servers, $disabledServers);
    }
