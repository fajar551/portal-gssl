  public function ModuleBuildParams($serviceID, $addonId = 0)
  {
    if ($addonId) {
      if (!$this->loadByAddonId($addonId)) {
        LogActivity::Save("Required Product Module '" . $this->getAddonModule() . "' Missing");
      }
    } else {
