  public function ServerUsageUpdate()
  {
    $servers = ServerModel::where("disabled", "0")->orderBy("name", "ASC")->get();
    $updatedServerIds = array();
    foreach ($servers as $serverModel) {
      $this->load($serverModel->type);
      if ($this->functionExists("UsageUpdate")) {
        $updatedServerIds[] = $serverModel->id;
        $response = $this->call("UsageUpdate", $this->getServerParams($serverModel));
        if ($response && !in_array($response, array("success", self::FUNCTIONDOESNTEXIST))) {
          LogActivity::Save("Server Usage Update Failed: " . $response . " - Server ID: " . $serverModel->id);
        }
      }
    }
    return $updatedServerIds;
  }

