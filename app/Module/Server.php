                if ($entityType == "addon") {
                  $updatearray = array("status" => "Active", "termination_date" => "0000-00-00");
                  $extraSaveData["suspend_reason"] = "";
                }
              } else {
                if ($function == "Terminate") {
                  if ($entityType == "addon") {
                    $updatearray = array("status" => "Terminated");
                    if (in_array(DB::table("tblhostingaddons")->where("id", "=", $entityId)->value("termination_date"), array("0000-00-00", "1970-01-01"))) {
